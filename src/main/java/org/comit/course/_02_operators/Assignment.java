package org.comit.course._02_operators;

public class Assignment {

	public static void main(String[] args) {
	
		int a = 4;
		System.out.println(a);

		a = a +5;
		a+=5;
		
		a++; // Post increment: delivers the value and then increment
		++a;
		
		
		a-=5;
		
		a*=5;
		
		a/=5;
		System.out.println(a);
	
	}

}

package org.comit.course._02_operators;

public class Logical {

	public static void main(String[] args) {
		boolean a = true;
		boolean b = false;
		
		boolean and = a && b;
		boolean or = a || b;
		boolean not = !a;
		
		System.out.println(and);
		System.out.println(or);
		System.out.println(not);

	}

}
